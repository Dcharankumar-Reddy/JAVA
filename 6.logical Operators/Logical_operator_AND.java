// logical operators
// logical AND-&
// logical OR-|
// logical XOR-^
// Short circuit OR-||
// short circuit AND-&&
// logical unary NOT-!

// Example on logical operators-&
class Logical_operator_AND
{
     public static void main(String arg[])
     {
        int  a=10,b=12,c=22;
         
        if (a>b & a>c)
            System.out.println("a  is big number");
         else 
            System.out.println("a is not big number");  
     }
}

