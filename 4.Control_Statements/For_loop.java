// Example on For loop
// java is a dynamic programming language 
class For_loop
{
    public static void main(String arg[])
    {
        int i,j;
        for (i=1;i<=5;i++)
         System.out.println(i);
        for ( j=10;j>=1;j--);
        System.out.println(j);
          
    }
    
        
}