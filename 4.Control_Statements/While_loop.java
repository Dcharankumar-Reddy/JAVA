// Example on Do while - by using natural numbers
public class While_loop {

    public static void main(String[] args) {
        int count = 1; // Start with the first natural number
        System.out.println("First 10 Natural Numbers:");

        // Loop to print the first 10 natural numbers
        while (count <= 10) {
            System.out.println(count);
            count++; // Increment count for the next number
        }
    }
}
