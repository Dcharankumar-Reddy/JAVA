// Example  on Do while- Dispaly the natural numbers
class Do_while_example
{
    public static void main (String arg[])
    {
        int n=1;
        do
        {
            System.out.println(n);
            n++;
        }
        while (n<=5);
    }
}
// Display the natural numbers from 10 to 1
class Do_while_example
{
    public static void main(String arg[])
    {
        int n=10;
        do{
            System.out.println(n);
            n--;
        }
        while (n>0);
    }
}