//Example on Switch case statement
class Switch_case
{
    public static void main (String arg[])
    {
        int grd=2;
        String res="";
        switch(grd)
        {
            case 1: res="First class";break;
            case 2: res="Second class";break;
            case 3: res="Third class";break;
            default: res="Fail";
        System.out.println("The Result is"+res);
        }
    }
}