class Arthimatic_operators_problem1
{
    public static void main (String arg[])
    {
        int a,b,c,res1,res2,res3;
         a=5;
         b=10;
         c=20;
        res1=a*b*c;
        res2=(a*b)-(4*b/3);
        res3=50^2+10^2-2*50*10;
        System.out.println("the result 1 is"+res1);
        System.out.println("the result 2 is"+res2);
        System.out.println("the result 3 is"+res3);

    }
}