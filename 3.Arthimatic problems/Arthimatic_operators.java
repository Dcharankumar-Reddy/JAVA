//  Example on Arthimatic operators
class  Arthimatic_operators
{
    public static void  main(String arg[])
    {
        int a,b,res;
         a=10;
         b=23;
         res=a+b;
         System.out.println("a+b=="+res);
         res=a-b;
         System.out.println("a-b="+res);
         res=a*b;
         System.out.println("a*b="+res);
         res=a/b;
         System.out.println("a/b="+res);
         res=a%b;
         System.out.println("a%b="+res);
         res=++a;
         System.out.println("increment of a="+res);
         res=--a;
         System.out.println("decrement of a="+res);

    }
}