//Exmaple on Blocks Example 2
// Blocks can be nested
class Block_Example
{ 
    public static void main(String arg[])
    {
         {
            int a=12;
            System.out.println("Block a= "+a);

         }
         {
            int b=432;
            System.out.println("Block  b= "+b);
         }
         {
            double c=23.89;
            System.out.println("Block c= "+c);
         }
    }
}