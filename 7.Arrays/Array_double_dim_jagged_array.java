// EXample on Double Dimensional Arrays- with  jagged arrays
// jagged arrays= Irregular of rows and columns is called jagged arrys

class Array_double_dim_jagged_array
{
    public static void main(String x[])
    {
        int a[][]={{10+10},{40*2,50*2,60*2},{70-10,80-10}};
        System.out.println(a[0][0]);
        System.out.println(a[1][0]+" "+a[1][1]+" "+a[1][2]);
        System.out.println(a[2][0]+" "+a[2][1]);

    }
}