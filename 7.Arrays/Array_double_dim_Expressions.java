// EXample on Double Dimensional Arrays- with  Expressions
class Array_double_dim_Expressions
{
    public static void main(String x[])
    {
        int a[][]={{10+10,20+10,30+10},{40*2,50*2,60*2},{70-10,80-10,90-10}};
        System.out.println(a[0][0]+" "+a[0][1]+" "+a[0][2]);
        System.out.println(a[1][0]+" "+a[1][1]+" "+a[1][2]);
        System.out.println(a[2][0]+" "+a[2][1]+" "+a[2][2]);

    }
}